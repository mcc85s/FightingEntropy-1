Class _Faces
{
    [Object]          $Faces
    [Hashtable]        $Hash = @{
    
         0 =  32, 32, 32, 32 # "    "
         1 =  95, 95, 95, 95 # "____"
         2 = 175,175,175,175 # "¯¯¯¯"
         3 =  45, 45, 45, 45 # "----"
         4 =  32, 32, 32, 47 # "   /"
         5 =  92, 32, 32, 32 # "\   "
         6 =  32, 32, 32, 92 # "   \"
         7 =  47, 32, 32, 32 # "/   "
         8 =  92, 95, 95, 47 # "\__/"
         9 =  47,175,175, 92 # "/¯¯\"
        10 =  47,175,175,175 # "/¯¯¯"
        11 = 175,175,175, 92 # "¯¯¯\"
        12 =  92, 95, 95, 95 # "\___"
        13 =  95, 95, 95, 47 # "___/"
        14 =  91, 32, 95, 95 # "[ __"
        15 =  95, 95, 32, 93 # "__ ]"
    }

    _Faces()
    {
        $This.Faces = @( )

        ForEach ( $I in 0..( $This.Hash.Count - 1 ) )
        {
            $This.Faces += ( [Char[]]$This.Hash[$I] -join '' )
        }
    }
}
